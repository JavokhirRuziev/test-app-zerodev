import { toast } from "react-toastify";
import { Bounce } from "react-toastify";

export const success = () => {
  toast.success("Success!", {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: true,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    transition: Bounce,
  });
};

